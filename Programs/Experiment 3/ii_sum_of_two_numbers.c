// Qn: Read two numbers, add them and display their sum

#include<stdio.h>
int main()
{
    int a,b,sum;
    printf("Enter the first number: ");
    scanf("%d",&a);
    printf("Enter the second number: ");
    scanf("%d",&b);
    sum=a+b;
    printf("Sum = %d",sum);
}

/*
Output:
Enter the first number: 2
Enter the second number: 6
Sum = 8
*/